                                        <h3><a href="#">Academic Curriculum Details</a></h3>
                                                                                                                            <p>1. Our school follows the CBSE curriculum.</p>

<p>2. Please refer to the official website&nbsp;<a href="http://cbse.nic.in/">cbse.nic.in.</a>&nbsp;&amp;&nbsp;<a href="http://cbseacademic.nic.in/">cbseacademic.nic.in</a></p>

<p>3. As per the instructions, we follow the NCERT books as prescribed by the CBSE.</p>

<p>4. Kindly refer to Uniform System of Assessment, Examination and Report Card for Classes VI-IX from Academic Year 2018-19 on wards mentioned in the school diary as well as on the CBSE website.</p>

<p>5.&nbsp;<strong>Syllabus:</strong>&nbsp;<a href="http://cbseacademic.nic.in/curriculum.html">http://cbseacademic.nic.in/curriculum.html</a></p>

<p>6.&nbsp;<strong>Primary School Program (I - V):</strong>&nbsp;The Primary School Program encourages inquiry and exploration, and develops children&#39;s competence in various skills - reading, writing, listening, speaking, problem-solving, observation, measurement and use of information and communications technology.The wide range of curricular and co-curricular opportunities offered helps instil in children confidence and discipline. The focus on developing communication skills helps them appreciate the value of education, as it reflects in their ability to express themselves, in addition to developing pragmatic skills.</p>

<p>7.&nbsp;<strong>Secondary School Program (VI - X):</strong>&nbsp;The Secondary School Program is structured to meet children&#39;s varied intellectual and developmental needs. The curriculum builds on concepts and skills learnt in the Primary School. It ensures progression and continuity across various stages of learning in the school, and incorporates continuous evaluation and review of children&#39;s performance.The main aims of the Programme are developing skills related to writing, reading, reflecting, critical thinking, public speaking, fostering initiative among students and a desire to acquire knowledge.</p>

<p>8.<strong>Senior Secondary Curriculum:</strong>&nbsp;In an effective classroom students should not only know what they are doing they should also know why and how. &nbsp;Our Educational Philosophy revolves around 3 Es i.e. Experiment Exploration and Experience which eventually lead to excellence. We believe in undertaking such scholarly practices which expand the horizon of our students, equipping them with the knowledge integral to becoming Global citizens and values enabling them to follow the humanistic path. &nbsp;It is our sincere endeavor to impart quality education to children in a stress-free, child friendly and child centric environment, through innovative and new ideas which maximize the process of learning.</p>

<p>Affiliated to CBSE, BPSN follows the framework of NCERT in letter and spirit.&nbsp;With technology walking into the classrooms, learning has become a joyous experience for auditory, visual and kinesthetic learners. &nbsp;Smart Class has converted every classroom from nursery to class XII into learning laboratory. Following the maxim of &lsquo;learning by doing&rsquo; teachers leave no stone unturned to become involved and be participative in accomplishing the aim of teaching learning process. The selection of books till class VIII and other resource material is carried out with a view of keeping alive the creative urge of a young child. The Middle, Secondary and Senior Secondary segments, from class VI to XII follow the line of action laid down by CBSE in accordance with NCERT.</p>

<p>Apart from offering core subjects at all levels, the school offers languages like Sanskrit AND Marathi to chose from as III language other than English and Hindi from classes IV to VIII, the same gets converted to II language from class XI. In Senior Secondary segment, the students can pursue Science or Commerce with a variety of Optional Subjects to choose from- Physical Education, Informatics Practices, Economics, Science and Mathematics.&nbsp;</p>

<p>This all encompassing education is systematized and planned, triggering in a student, an insatiable thirst for knowledge.</p>

